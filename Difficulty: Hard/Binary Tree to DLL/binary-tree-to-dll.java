class Solution
{
    //Function to convert binary tree to doubly linked list and return it.
       
    Node prev = null;
    Node head = null; 
    Node bToDLL(Node root)
    {
	//  Your code here
	if (root == null) {
            return null; // If the tree is empty, return null
        }

        // Perform in-order traversal to convert the binary tree to DLL
        inOrder(root);
        
        return head; // Head points to the first node in DLL
    }
     void inOrder(Node root)
    {
        if (root == null) {
            return;
        }

        // Traverse the left subtree
        inOrder(root.left);

        // Now process the current node
        if (prev == null) {
            // If prev is null, it means this is the first node (leftmost node)
            head = root; // This node will be the head of the DLL
        } else {
            // Link the current node with the previous node
            prev.right = root;
            root.left = prev;
        }

        // Update prev to the current node
        prev = root;

        // Traverse the right 
        inOrder(root.right);
    }
}