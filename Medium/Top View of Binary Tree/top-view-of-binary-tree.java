class Pair{
    Node node;
    int vertical;
    public Pair(Node node,int vertical){
        this.node=node;
        this.vertical=vertical;
    }
}
class Solution
{
    //Function to return a list of nodes visible from the top view 
    //from left to right in Binary Tree.
    static ArrayList<Integer> topView(Node root)
    {
        // add your code
        ArrayList<Integer> ans=new ArrayList<>();
        Queue<Pair> q1=new LinkedList<>();
        TreeMap<Integer,Integer> map=new TreeMap<>();
        q1.add(new Pair(root,0));
        while(!q1.isEmpty()){
            Pair p1=q1.remove();
            int verti=p1.vertical;
            Node temp=p1.node;
            if(!map.containsKey(verti)){
                map.put(verti,temp.data);
            }
            if(temp.left!=null){
                q1.add(new Pair(temp.left,verti-1));
            }
            if(temp.right!=null){
                q1.add(new Pair(temp.right,verti+1));
            }
        }
        for(Integer it:map.keySet()){
            ans.add(map.get(it));
        }
        return ans;
        
    }
}